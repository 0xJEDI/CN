// Numbers = Random Number * 100 To Equal A Whole Number
let numbers = Math.floor(Math.random() * 100);

// Defines the number as an Age
let ages = numbers

// Defines The Age
let Age = 18

// While The Random Number Gen Is Less Than Defined Age
while (ages < Age){
    // Then Print Ur a child!
    console.log(`You're a child! ${ages}`)
}

// Else Print ur an Adult!
console.log(`You're an adult! ${ages}`)